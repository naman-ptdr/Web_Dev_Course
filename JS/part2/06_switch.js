let color = "red";
switch(color){
    case "red" :
        console.log(`Stop! Light color is ${color}`);
        break;
    case "yellow" :
        console.log(`Slow Down! Light color is ${color}`);
        break;
    case "green" :
        console.log(`Go! Light color is ${color}`);
        break;
    default:
        console.log(`Light is broken!`);
}